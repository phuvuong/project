    public function delete_user_roles( $admin_id)
    {
        $this->AuthLogin();
       if(Auth::id()==$admin_id){
        return redirect()->back()->with('error','Bạn không được xóa chính mình');
       }
       $admin = Admin::find($admin_id);
       if($admin){
        $admin->role()->detach();
        $admin->delete();
        return redirect()->back()->with('message','Xóa tài khoản thành công');
       }
    

    }

